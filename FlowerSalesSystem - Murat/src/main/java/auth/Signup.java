    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
        String username = txtUserName.getText();
        String email = txtEmail.getText();
        String password = new String(txtPassword.getPassword());

        String emailRegex = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$";
        String nameRegex = "^[A-Za-z]+$";

        if (!email.matches(emailRegex)) {
            JOptionPane.showMessageDialog(this, "Invalid e-mail address. Please enter a valid email address.", "Error", JOptionPane.ERROR_MESSAGE);
            txtEmail.setText("");
            return;
        }

        if (!username.matches(nameRegex)) {
            JOptionPane.showMessageDialog(this, "Invalid name. Please enter a name containing only letters.", "Error", JOptionPane.ERROR_MESSAGE);
            txtUserName.setText("");
            return;
        }

        User user = new User(username, password, email);
        UserDatabaseManager.register(user);

        Login l = new Login();
        l.setVisible(true);
        dispose();
    }//GEN-LAST:event_btnSignupActionPerformed

