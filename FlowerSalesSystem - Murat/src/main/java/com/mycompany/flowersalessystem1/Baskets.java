    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int selectedRow = tblBasket.getSelectedRow();

        if (selectedRow >= 0) {
            // Get row information
            String flowerIdName = (tblBasket.getValueAt(selectedRow, 1).toString());
            int flowerIdDouble = FlowerDatabaseManager.getFlowerIdByName(flowerIdName);
            double quantityDouble = Double.parseDouble(tblBasket.getValueAt(selectedRow, 2).toString());

            // Round and convert to integers
            int flowerId = (int) Math.round(flowerIdDouble);
            int quantity = (int) Math.round(quantityDouble);

            // Update the quantity of the basket item or delete it if quantity is 1
            UserDatabaseManager.removeFromBasket(UserDatabaseManager.getUserIdByEmail(this.u.getEmail()), flowerId, 1);

            // Update the stock quantity of the flower
            FlowerDatabaseManager.updateFlowerStock(flowerId, FlowerDatabaseManager.getFlowerStock(flowerId) + 1);

            // Update the table
            updateBasketTable();
        } else {
            // If no row is selected, display an error message
            JOptionPane.showMessageDialog(this, "Please select an item to remove.", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnRemoveActionPerformed

