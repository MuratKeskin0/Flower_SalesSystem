    public void updateBasketTable() {
        int userId = UserDatabaseManager.getUserIdByEmail(this.u.getEmail());
        List<BasketItem> basketItems = BasketDatabaseManager.getUserBasket(userId);

        DefaultTableModel model = (DefaultTableModel) tblBasket.getModel();
        model.setRowCount(0); // clear the existing table

        for (BasketItem item : basketItems) {
            Flower flower = item.getFlower();
            int quantity = item.getQuantity();

            model.addRow(new Object[]{flower.getPrice(), flower.getName(), quantity});
        }
        updateTotalPrice();
    }

    private void updateTotalPrice() {
        double total = 0;
        for (int i = 0; i < t.getRowCount(); i++) {
            double price = (double) t.getValueAt(i, 0); //assuming "price" is at index 0
            int quantity = (int) t.getValueAt(i, 2); //assuming "quantity" is at index 2
            total += price * quantity;
        }
        txtTotalPrice.setText(String.format("%.2f", total));
    }

